                      <h3 id="ads-claims">Advertising & Income/Health Claims</h3>
                      <ul>
                        <li><strong>No misleading conduct:</strong> Avoid exaggerated “get rich quick” or unsubstantiated health claims.</li>
                        <li><strong>Income disclosures:</strong> Use disclaimers and, where possible, publish average earnings ranges.</li>
                        <li><strong>Therapeutic claims:</strong> If making health claims, ensure TGA compliance and evidence.</li>
                        <li><strong>Testimonials:</strong> Keep proof; disclose if incentivised.</li>
                      </ul>

                      <h3 id="privacy-spam">Privacy (APPs) & Spam Act 2003</h3>
                      <ul>
                        <li><strong>Consent & Purpose:</strong> Collect/store personal data for defined purposes only.</li>
                        <li><strong>Access & Security:</strong> Provide access on request; secure data (encryption, role-based access).</li>
                        <li><strong>Marketing Consent:</strong> For email/SMS, obtain express consent or ensure it meets inferred consent rules.</li>
                        <li><strong>Identify & Unsubscribe:</strong> Messages must identify sender and include a working, easy opt-out.</li>
                      </ul>

                      <h3 id="tax-gst">Tax, GST & Payouts</h3>
                      <ul>
                        <li><strong>GST registration:</strong> If turnover exceeds the threshold, register and issue tax invoices (ABN, GST amounts, etc.).</li>
                        <li><strong>Commission payouts:</strong> Track wallet ledgers, apply withholding/fees, export summaries for BAS/accounting.</li>
                        <li><strong>Distributor status:</strong> Distributors may operate as sole traders/companies; consider ABN collection and GST handling per distributor.</li>
                      </ul>

                      <h3 id="contracts-refunds">Contracts, Cooling-off & Refunds</h3>
                      <ul>
                        <li><strong>Transparent terms:</strong> Compensation plan, return policy, code of conduct, data handling.</li>
                        <li><strong>Cooling-off:</strong> Provide reasonable cooling-off for business kits/subscriptions where applicable.</li>
                        <li><strong>Refunds & warranties:</strong> Honour ACL consumer guarantees; outline RMA flow in your software.</li>
                      </ul>

