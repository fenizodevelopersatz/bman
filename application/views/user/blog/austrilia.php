                      <h3 id="legal-framework">Legal Framework at a Glance</h3>
                      <ul>
                        <li><strong>Australian Consumer Law (ACL):</strong> Prohibits <em>pyramid schemes</em>, misleading or deceptive conduct, and unfair practices.</li>
                        <li><strong>ACCC oversight:</strong> Marketing conduct, product claims, refunds, representations.</li>
                        <li><strong>ASIC (financial products):</strong> If your plan touches financial products, additional licensing & disclosure may apply.</li>
                        <li><strong>TGA (therapeutic goods):</strong> Health products/claims must follow TGA rules; some goods require ARTG listing or registration.</li>
                        <li><strong>Privacy Act & APPs:</strong> Handle personal data under the Australian Privacy Principles (consent, purpose, access, security).</li>
                        <li><strong>Spam Act 2003:</strong> Consent, identification and unsubscribe requirements for email/SMS.</li>
                        <li><strong>Fair Trading (states/territories):</strong> Local obligations may also apply.</li>
                      </ul>

                      <h3 id="pyramid-scheme">Pyramid Scheme vs. Lawful MLM</h3>
                      <p>
                        Under the ACL, a <strong>pyramid scheme</strong> is illegal. Lawful MLM focuses on
                        <strong>genuine consumer sales</strong> and value delivery. Key differences:
                      </p>
                      <ul>
                        <li><strong>Legitimate MLM:</strong> Rewards primarily from product/service sales to customers; clear refund policies; realistic earnings.</li>
                        <li><strong>Illegal pyramid:</strong> Rewards mainly for recruiting; little/no focus on real customer demand.</li>
                      </ul>
                      <p class="mn-callout">
