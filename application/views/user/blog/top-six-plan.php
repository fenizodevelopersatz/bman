                    <h3 id="binary">1. Binary Plan</h3>
                    <p>
                      Members build <strong>two legs (left & right)</strong>. Commissions are based on matching sales volume on both sides (pairs).
                    </p>
                    <ul>
                      <li><strong>Pros:</strong> Simple, team-driven, spillover helps weak leg.</li>
                      <li><strong>Cons:</strong> Can bottleneck on weak leg; requires balancing.</li>
                    </ul>

                    <h3 id="matrix">2. Matrix Plan</h3>
                    <p>
                      A <strong>forced matrix</strong> like 3×3 or 5×7 limits frontline width and pays fixed depth. 
                      Spillover ensures teamwork.
                    </p>
                    <ul>
                      <li><strong>Pros:</strong> Predictable structure, depth growth.</li>
                      <li><strong>Cons:</strong> Can stall if uplines inactive.</li>
                    </ul>

                    <h3 id="unilevel">3. Unilevel Plan</h3>
                    <p>
                      Unlimited frontline recruits. Commission paid down multiple levels (e.g., 7–10 levels), often with activity qualifiers.
                    </p>
                    <ul>
                      <li><strong>Pros:</strong> Simple, unlimited width, great for strong recruiters.</li>
                      <li><strong>Cons:</strong> Needs qualifiers to prevent overpayment.</li>
                    </ul>

                    <h3 id="generation">4. Generation / Breakaway Plan</h3>
                    <p>
                      Rewards are paid based on <strong>rank-based generations</strong>. Downline leaders “break away” to form new groups.
                    </p>
                    <ul>
                      <li><strong>Pros:</strong> Rewards leadership development.</li>
                      <li><strong>Cons:</strong> More complex tracking.</li>
                    </ul>

                    <h3 id="board">5. Board / Cycler Plan</h3>
                    <p>
                      Members enter a <strong>board</strong> (e.g., 2×2). When the board fills, the top cycles out and receives payout; others move up.
                    </p>
                    <ul>
                      <li><strong>Pros:</strong> Exciting, gamified, fast turnover.</li>
                      <li><strong>Cons:</strong> High churn; complex to explain.</li>
                    </ul>

                    <h3 id="gift">6. Gift / Donation Plan</h3>
                    <p>
                      Peer-to-peer <strong>donation plans</strong> where members gift fixed amounts and later receive from their downline or cycle.
                    </p>
                    <ul>
                      <li><strong>Pros:</strong> Simple to start, low entry cost.</li>
                      <li><strong>Cons:</strong> High legal risk (often seen as pyramid schemes).</li>
                    </ul>

