                    <h3 id="how-it-works">How Matrix MLM Works (Width × Depth)</h3>
                    <p>
                        In a <strong>3×3</strong> matrix, each member can directly sponsor up to 3 people on
                        their first level. The plan pays commissions up to 3 levels deep. New signups beyond
                        the width limit are placed in the first available position below (spillover),
                        encouraging team building and depth growth.
                    </p>

