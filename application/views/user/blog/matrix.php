                    <h3 id="payout-methods">Payout Methods in Matrix</h3>
                    <ul>
                        <li><strong>Level Commission:</strong> Fixed/percentage payout per level (e.g., L1=₹200, L2=₹150, L3=₹100).</li>
                        <li><strong>Product/Service PV:</strong> Commissions derived from PV attached to SKUs (e.g., 10 PV per subscription).</li>
                        <li><strong>Cumulative Pool:</strong> A pool from a share of sales volume, split among qualifiers.</li>
                    </ul>

                    <h3 id="bonuses">Common Bonuses to Mix with Matrix</h3>
                    <ul>
                        <li><strong>Referral Bonus:</strong> Direct sponsor reward on every personal signup.</li>
                        <li><strong>Matching Bonus:</strong> Earn a percent matching of your downline’s matrix income.</li>
                        <li><strong>Rank Bonus:</strong> Extra payout on crossing rank thresholds (volume + active legs).</li>
                        <li><strong>Re-entry / Refill:</strong> Auto-create a new position once a matrix/cycle completes.</li>
                    </ul>

                    <h3 id="example-33">Worked Example: 3×3 Matrix (Simple)</h3>
                    <p>Assume level commissions per active position: L1 = ₹200, L2 = ₹150, L3 = ₹100.</p>
                    <ul>
                        <li>Level 1: 3 members × ₹200 = ₹600</li>
                        <li>Level 2: 9 members × ₹150 = ₹1,350</li>
                        <li>Level 3: 27 members × ₹100 = ₹2,700</li>
                    </ul>
                    <p><strong>Total potential per filled 3×3 = ₹4,650.</strong> Actuals vary with qualifiers, activity rules, and caps.</p>

