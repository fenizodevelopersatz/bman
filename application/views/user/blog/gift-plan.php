                      <h3 id="how-it-works">How a Gift MLM Works</h3>
                      <p>
                        Each member <strong>gifts</strong> a fixed amount to an upline when they join. 
                        In exchange, they unlock eligibility to receive gifts from their own downline
                        according to plan rules (levels, matrix, or board cycles).
                      </p>
                  

                      <h3 id="payout-structure">Donation & Payout Structure</h3>
                      <ul>
                        <li><strong>Fixed entry gift:</strong> Eg. $50 gifted to sponsor/upline.</li>
                        <li><strong>Level-based:</strong> Receive from X number of members on Y levels.</li>
                        <li><strong>Board/Cycle:</strong> Once a board fills, the top receives pooled gifts and re-enters.</li>
                        <li><strong>Spillover:</strong> Extra signups spill to downline, encouraging teamwork.</li>
                      </ul>

